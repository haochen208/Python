# 定义score函数，传入一个str参数，预设一组词典（见下方），根据词典中字母对应的数值，
# 将传入参数的每个字母数值相加（非字母不加，注意大小写转换）
# 如"word!"，分别取"w":11,"o":3,"r":6,"d":4，相加为11+3+6+4=24 ，return该值
# 预设字典：
s = {'a':1,'b':2,'c':3,'d':4,'e':5,'f':6,'g':7,'h':8,'i':9,'j':10,'k':11,'l':12,'m':1,
     'n':2,'o':3,'p':4,'q':5,'r':6,'s':7,'t':8,'u':9,'v':10,'w':11,'x':12,'y':13,'z':14}
def score(str):
    m=0
    for i in str.lower():
        if i in s.keys():
            m += s[i]
    return m
print(score("word!"))