# 集合是可变类型：一般的可变类型增删改查都支持，集合只支持增和删，不支持改和查。

# 增加：add()只能增加一个值，不能增加多个值。
# s1 = {1, 2, 3, 4}
# s1.add(5)
# print(s1)
# s1.add((5, 6))
# print(s1)

# 删除：remove()删除指定元素。
# s2 = {"a", "b", "c", "d"}
# s2.remove("c")
# print(s2)

# 查找：不支持，无法通过索引查找对应的值。

# 修改：不支持，集合里面的不可变类型数据不能被修改。



# 六种数据类型：
# 数字：都不支持
# 字符串：查
# 列表：增删改查
# 元组：查
# 字典：增删改查
# 集合：增删
